package model;

public class FindProduct {
	private String category_name;
	private String stall_location;
	private String stall_name;
	private String stall_img;
	private int stall_storeid;
	public String getCategory_name() {
		return category_name;
	}
	public void setCategory_name(String category_name) {
		this.category_name = category_name;
	}
	public String getStall_location() {
		return stall_location;
	}
	public void setStall_location(String stall_location) {
		this.stall_location = stall_location;
	}
	public String getStall_name() {
		return stall_name;
	}
	public void setStall_name(String stall_name) {
		this.stall_name = stall_name;
	}
	public String getStall_img() {
		return stall_img;
	}
	public void setStall_img(String stall_img) {
		this.stall_img = stall_img;
	}
	public int getStall_storeid() {
		return stall_storeid;
	}
	public void setStall_storeid(int stall_storeid) {
		this.stall_storeid = stall_storeid;
	}
	

}
